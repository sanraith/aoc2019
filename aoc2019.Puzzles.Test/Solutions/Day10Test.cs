using aoc2019.Puzzles.Solutions;
using NUnit.Framework;
using System.Threading.Tasks;

namespace aoc2019.Puzzles.Test.Solutions
{
    public sealed class Day10Test : TestBase<Day10>
    {
        [Test]
        public async Task Part1()
        {
            var input = @".#..#
.....
#####
....#
...##";
            Assert.That(await Solution.Part1Async(input), Is.EqualTo("8"));

            input = @"......#.#.
#..#.#....
..#######.
.#.#.###..
.#..#.....
..#....#.#
#..#....#.
.##.#..###
##...#..#.
.#....####";
            Assert.That(await Solution.Part1Async(input), Is.EqualTo("33"));

            input = @"#.#...#.#.
.###....#.
.#....#...
##.#.#.#.#
....#.#.#.
.##..###.#
..#...##..
..##....##
......#...
.####.###.";
            Assert.That(await Solution.Part1Async(input), Is.EqualTo("35"));

            input = @".#..#..###
####.###.#
....###.#.
..###.##.#
##.##.#.#.
....###..#
..#.#..#.#
#..#.#.###
.##...##.#
.....#.#..";
            Assert.That(await Solution.Part1Async(input), Is.EqualTo("41"));

            input = @".#..##.###...#######
##.############..##.
.#.######.########.#
.###.#######.####.#.
#####.##.#.##.###.##
..#####..#.#########
####################
#.####....###.#.#.##
##.#################
#####.##.###..####..
..######..##.#######
####.##.####...##..#
.#####..#.######.###
##...#.##########...
#.##########.#######
.####.#.###.###.#.##
....##.##.###..#####
.#.#.###########.###
#.#.#.#####.####.###
###.##.####.##.#..##";
            Assert.That(await Solution.Part1Async(input), Is.EqualTo("210"));
        }

        [Test]
